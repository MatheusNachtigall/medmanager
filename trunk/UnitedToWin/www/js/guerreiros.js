function validaCamposTreinamento( )
{
	var atual = parseInt($('#nvAtual').val());
    var desejado = parseInt($('#nvDesejado').val());
    var txt = "";

    var classe =  parseInt($('#classe').val());
    

    if( !((atual <= 0) || (desejado > 100)|| (isNaN(atual)) || (isNaN(desejado)) || (desejado < atual))) 
    {  
        var soma_tempo = 0, soma_diamantes = 0, i;
        
        switch (classe) {
            case 1: 
                for (i = atual; i < desejado; i++) { 
                    soma_tempo += opcoes_tempo[i].Guerreiro;
                    soma_diamantes += opcoes_diamante[i].Guerreiro;
                }
                break;
        
            case 2: 
                for (i = atual; i < desejado; i++) { 
                    soma_tempo += opcoes_tempo[i].Carpinteiro;
                    soma_diamantes += opcoes_diamante[i].Carpinteiro;
                }
                break;
        
            case 3: 
                for (i = atual; i < desejado; i++) { 
                    soma_tempo += opcoes_tempo[i].Ferreiro;
                    soma_diamantes += opcoes_diamante[i].Ferreiro;
                }
                break;
        
            case 4: 
                for (i = atual; i < desejado; i++) { 
                    soma_tempo += opcoes_tempo[i].Outros;
                    soma_diamantes += opcoes_diamante[i].Outros;
                }
                break;
        }
        
        var dias = Math.floor(soma_tempo/1440);
        if (dias > 0) {
            txt += dias + " Dias ";
        }
        soma_tempo = soma_tempo%1440;
        var horas = Math.floor(soma_tempo/60);
        if (horas > 0) {
            txt += horas + " Horas ";
        }
        soma_tempo = soma_tempo%60;
        if (soma_tempo > 0) {
            txt += soma_tempo + " Minutos";
        }
        $("#respTreinamento").text(txt);
        txt = soma_diamantes + " Diamantes"
        $("#respDiamantes").text(txt);
    }
}



var calculaTreinamento = function() {
    var buscaTimeout = -1;
    if (buscaTimeout != -1) clearInterval(buscaTimeout);
        buscaTimeout = setTimeout(function() {
        buscaTimeout = -1;

        validaCamposTreinamento();
    
    }, 500);
}


$('#nvAtual,#nvDesejado').keyup(calculaTreinamento);